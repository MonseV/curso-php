<?php
    $num = 10;

    $num += 5; // es lo mismo que: $num = $num + 5;
    var_dump($num); // 15

    echo "<br>";

    $num -= 2; // es lo mismo que: $num = $num - 2;
    var_dump($num); // 13

    echo "<br>";

    $num *= 4; // es lo mismo que: $num = $num * 4;
    var_dump($num); // 52
?>